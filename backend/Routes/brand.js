const express = require('express');
const router = express.Router();
const brandController = require('../Controller/brand');
const authMiddleware = require('../Middleware/Auth');

router.patch('/updateBrand/:id',authMiddleware(['admin']), brandController.update); //edit a brand
router.get('/viewBrand/:sellerId', authMiddleware(['admin']), brandController.getAllBySeller); //view all brands of a seller

module.exports = router;








































































